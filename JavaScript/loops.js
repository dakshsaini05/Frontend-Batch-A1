// for-of loop (to get the element of the array or string)
let str = "Daksh"
for(let s of str) {
    console.log(s)
}

let arr = [1,2,3,4,5]
for(let a of arr) {
    console.log(a)
}

// for-in loop (to get the index/keys of a array or a string)
for(let i in str) {
    console.log(i)
}

for(let i in arr) {
    console.log(i)
}
